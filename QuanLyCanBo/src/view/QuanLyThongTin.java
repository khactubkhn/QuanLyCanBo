    private void jbXoaCBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbXoaCBActionPerformed
        // TODO add your handling code here:
       int index = jTable_QuanLyThongTin.getSelectedRow();
       String maCanBo = jtfMaCB.getText();
       String tenCanBo = jtfTenCanBo.getText();
       String mcb = "%" + maCanBo.trim() + "%";
       String tencb = "%" + tenCanBo.trim() + "%";
       arrayList = dBCanBo.findBy(mcb, tencb);

        if (index >= 0) {
            CanBo canBo = arrayList.get(index);
            int conf = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa dòng  : Mã Cán Bộ :  " + canBo.getMaCB() + "Tên Cán bộ  =  " + canBo.getHoTen(), "Thông báo!!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (conf == JOptionPane.YES_OPTION) {
                canBo = dBCanBo.deleteCanBo(canBo);
                if (canBo != null) {
                    arrayList.remove(index);
                    loadDataTable(arrayList);
                    JOptionPane.showMessageDialog(null, "Xóa thành công !!");
                } else {
                    JOptionPane.showMessageDialog(null, "Xóa thất bại ");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn dòng để xóa !!", "Thông báo ", JOptionPane.WARNING_MESSAGE);
        } 
    }//GEN-LAST:event_jbXoaCBActionPerformed

