    private void loadDataTable(ArrayList<CanBo> al) {
//        arrayList = dBChamThi.getAll();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        //Làm mới bảng
        defaultTableModel.setRowCount(0);

        ArrayList<Object> listRow = new ArrayList<>();
        if (al != null) {
            for (int i = 0; i < al.size(); i++) {
                listRow.add(i + 1);
                listRow.add(al.get(i).getMaCB());
                listRow.add(al.get(i).getHoTen());
                Date date = al.get(i).getNgaySinh();
                String ngaySinh = dateFormat.format(date);
                listRow.add(ngaySinh);
                listRow.add(al.get(i).getGioiTinh());
                listRow.add(al.get(i).getMaSoThue());
                listRow.add(al.get(i).getSoTaiKhoan());
                listRow.add(al.get(i).getDiaChi());
                listRow.add(al.get(i).getSDT());
                listRow.add(al.get(i).getEmail());
                listRow.add(al.get(i).getPCGiangDay());
                listRow.add(al.get(i).getThanhTichKhac());
                listRow.add(al.get(i).getUser_level());
                listRow.add(al.get(i).getPassword());
                Date date1 = al.get(i).getNgayBD();
                String ngayBD = dateFormat.format(date1);
                listRow.add(ngayBD);
                defaultTableModel.addRow(listRow.toArray());
                listRow.clear();
            }
            jTable_QuanLyThongTin.setModel(defaultTableModel);
        }
    }
